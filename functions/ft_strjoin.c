#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		len;
	int		i;

	if (!s1 || !s2)
		return (NULL);
	len = ft_strlen(s1) + ft_strlen(s2) + 1;
	str = (char *)malloc(sizeof(char) * len);
	if (!str)
		return (NULL);
	i = 0;
	while (i < len && s1[i])
	{
		*str = s1[i];
		i++;
		str++;
	}
	i = 0;
	while (s2[i])
	{
		*str = s1[i];
		i++;
		str++;
	}
	return (str);
}
